# Manipulacje cytatami
# Demonstruje metody lancucha

# Cytat z wypowiedzi prezesa IBM, Thomasa Watsona, z 1943 r.
quote = "Myślę, że istnieje światowy rynek dla może pięciu komputerów."

print("Oryginalny cytat w tlumaczeniu na jezyk polski:")
print(quote)

print("\nDuzymi literami:")
print(quote.upper())

print("\nMalymi literami:")
print(quote.lower())

print("\nWszystkie wyrazy od duzej litery:")
print(quote.title())

print("\nZ drobna zamiana:")
print(quote.replace("pięciu", "milionów"))

print("\nOryginalny cytat pozostaje bez zmian")
print(quote)

