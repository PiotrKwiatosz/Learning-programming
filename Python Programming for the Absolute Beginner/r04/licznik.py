# Licznik
# Demonstruje funkcje 'range()'

print("Liczenie:")
for i in range(10):
    print(i, end=" ")

print("\n\nLicznenie co piec:")
for i in range(0, 50, 5):
    print(i, end=" ")

print("\n\nLiczenie do tylu:")
for i in range(10, 0, -1):
    print(i, end=" ")

